    private void updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateMouseClicked
        int id = Integer.parseInt(String.valueOf(model.getValueAt(this.list.getSelectedRow(), 0)));
        String title = this.title.getText();
        int quantity = Integer.parseInt(this.quantity.getText());
        float price = Float.parseFloat(this.price.getText());
        
        Product product = new Product(
            id,
            title, 
            quantity, 
            price
        );
        
        product.save();
        
        this.model.setValueAt(this.title.getText(), this.list.getSelectedRow(), 1);
        this.model.setValueAt(this.quantity.getText(), this.list.getSelectedRow(), 2);
        this.model.setValueAt(this.price.getText(), this.list.getSelectedRow(), 3);
    }//GEN-LAST:event_updateMouseClicked

