    private void updateTable() {
        
        List<Product> productList = Product.getProducts();

        for (Product p: productList) {
            this.model.insertRow(
                    model.getRowCount(),
                    new Object[] {p.getId(),
                        p.getTitle(),
                        p.getQantity(),
                        p.getPrice()
                    }
            );
        }
        
    }
    
